func Md5(str string) string {
	h := md5.New()
	h.Write([]byte(str))
	return hex.EncodeToString(h.Sum(nil))
}

func GbkToUtf8(str string) string {
	return mahonia.NewEncoder("gbk").ConvertString(str)
}

// snake string, XxYy to xx_yy , XxYY to xx_yy
func SnakeString(s string) string {
	data := make([]byte, 0, len(s)*2)
	j := false
	num := len(s)
	for i := 0; i < num; i++ {
		d := s[i]
		if i > 0 && d >= 'A' && d <= 'Z' && j {
			data = append(data, '_')
		}
		if d != '_' {
			j = true
		}
		data = append(data, d)
	}
	return strings.ToLower(string(data[:]))
}

// camel string, xx_yy to XxYy
func CamelString(s string) string {
	data := make([]byte, 0, len(s))
	j := false
	k := false
	num := len(s) - 1
	for i := 0; i <= num; i++ {
		d := s[i]
		if k == false && d >= 'A' && d <= 'Z' {
			k = true
		}
		if d >= 'a' && d <= 'z' && (j || k == false) {
			d = d - 32
			j = false
			k = true
		}
		if k && d == '_' && num > i && s[i+1] >= 'a' && s[i+1] <= 'z' {
			j = true
			continue
		}
		data = append(data, d)
	}
	return string(data[:])
}

// PlateNumberCheck 车牌校验
func PlateNumberCheck(plateNumber string) bool {
	r1 := regexp.MustCompile(plateNumberRegEx)
	r2 := regexp.MustCompile(plateNumberNewRegEx)
	if r1.MatchString(plateNumber) || r2.MatchString(plateNumber) {
		return true
	}
	return false
}

func TelPhoneNumberCheck(phoneNumber string) bool {
	reg := regexp.MustCompile(regular)
	return reg.MatchString(phoneNumber)
}
